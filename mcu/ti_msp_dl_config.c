    DL_UART_Main_init(DEFAULT_UART_INST, (DL_UART_Main_Config *) &gDEFAULT_UARTConfig);
    /*
     * Configure baud rate by setting oversampling and baud rate divisors.
     *  Target baud rate: 115200
     *  Actual baud rate: 115190.78
     */
    DL_UART_Main_setOversampling(DEFAULT_UART_INST, DL_UART_OVERSAMPLING_RATE_16X);
    DL_UART_Main_setBaudRateDivisor(DEFAULT_UART_INST, DEFAULT_UART_IBRD_40_MHZ_115200_BAUD, DEFAULT_UART_FBRD_40_MHZ_115200_BAUD);


    /* Configure Interrupts */
